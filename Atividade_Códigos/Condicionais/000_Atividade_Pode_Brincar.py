idade = int(input("Quantos anos você tem? "))

if idade > 10:
    print(f"\nVocê tem {idade} anos!")
    print("Você pode entrar no brinquedo, pois tem a idade permitida.")
else:
    print(f"\nVocê tem {idade} anos!")
    print("Você não pode entrar no brinquedo, pois a idade permitida é acima de 10 anos.")