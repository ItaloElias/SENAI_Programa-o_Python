km = float(input("Digite a distância em quilômetros: "))

metros = km * 1000
centimetros = km * 100000
milimetros = km * 1000000

print("\nConversão de unidades:")
print(f"\n{km} quilômetros equivalem a:")
print(f"\n{metros} metros\n")
print(f"{centimetros} centímetros\n")
print(f"{milimetros} milímetros \n")